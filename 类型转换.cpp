#include <stdio.h>
int main()
{
    int i=289;
    char c='a';
    c=i;
    printf("%c\n",c);//!
    /*
    *int 占四个字节，char占一个字节，int转换为char要截取后面的一个字节
    *289:00000001 00100001
    *             00100001(33)='!'
    */

    int a=32767;
    short b=a+1;
    printf("%d\n",b);//-32768
    /*
    *int占四个字节，short占两个字节
    *00000000 00000000 01111111 11111111+1
    *                  10000000 00000000(-32768)
    */
}
